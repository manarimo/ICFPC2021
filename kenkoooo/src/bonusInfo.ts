// 適当にその問題のそのボーナスを可能にしうる問題番号を返す
// あとのプロセスがその適当な番号を、それっぽい値に置き換えるとのこと
export const getPossibleBonusSourceProblemId = (
  problemId: number,
  bonusType: BonusType
) => {
  const candidate = FROM_TO_BONUSTYPE.find(
    ([from, to, bt]) => problemId === to && bt === bonusType
  );
  if (!candidate) {
    return -1;
  }
  return candidate[0];
};

export const getAvailableBonuses = (problemId: number) => {
  const candidates = FROM_TO_BONUSTYPE.filter(
    ([from, to, bt]) => problemId === to
  ).map(([from, to, bonusType]) => bonusType);
  return new Set(candidates);
};
